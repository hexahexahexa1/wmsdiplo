package com.wmsdipl.core.domain;

/**
 * Типы складских зон.
 * Определяет назначение зоны в процессах складской логистики.
 */
public enum ZoneType {
    
    /**
     * Зона приемки товара (транзитная зона).
     * Используется для временного размещения поступающего товара до его размещения на хранение.
     */
    RECEIVING("Приемка"),
    
    /**
     * Зона длительного хранения.
     * Основная зона для размещения товарных запасов.
     */
    STORAGE("Хранение"),
    
    /**
     * Зона отгрузки.
     * Используется для комплектации и подготовки заказов к отправке.
     */
    SHIPPING("Отгрузка"),
    
    /**
     * Карантинная зона.
     * Для изоляции товара, требующего проверки или имеющего ограничения.
     */
    QUARANTINE("Карантин");
    
    private final String displayName;
    
    ZoneType(String displayName) {
        this.displayName = displayName;
    }
    
    /**
     * Возвращает человекочитаемое название типа зоны.
     *
     * @return отображаемое имя
     */
    public String getDisplayName() {
        return displayName;
    }
}
