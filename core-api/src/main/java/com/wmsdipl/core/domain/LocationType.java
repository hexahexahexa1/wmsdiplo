package com.wmsdipl.core.domain;

/**
 * Типы складских ячеек.
 * Определяет назначение ячейки в процессах складской логистики.
 */
public enum LocationType {
    
    /**
     * Ячейка приемки товара (транзитная зона).
     * Используется для временного размещения поступающего товара до его размещения на хранение.
     */
    RECEIVING("Приемка"),
    
    /**
     * Ячейка длительного хранения.
     * Основная ячейка для размещения товарных запасов.
     */
    STORAGE("Хранение"),
    
    /**
     * Ячейка отгрузки.
     * Используется для комплектации и подготовки заказов к отправке.
     */
    SHIPPING("Отгрузка"),
    
    /**
     * Ячейка кросс-доккинга.
     * Для товаров, которые идут напрямую с приёмки на отгрузку без длительного хранения.
     */
    CROSS_DOCK("Кросс-доккинг"),
    
    /**
     * Ячейка для повреждённого товара.
     * Для товаров с браком, ожидающих списания или возврата поставщику.
     */
    DAMAGED("Брак"),
    
    /**
     * Карантинная ячейка.
     * Для изоляции товара, требующего проверки или имеющего ограничения.
     */
    QUARANTINE("Карантин");
    
    private final String displayName;
    
    LocationType(String displayName) {
        this.displayName = displayName;
    }
    
    /**
     * Возвращает человекочитаемое название типа ячейки.
     *
     * @return отображаемое имя
     */
    public String getDisplayName() {
        return displayName;
    }
}
